let lives = 6;

// Getting a random animal from the list
let number = Math.floor(Math.random() * animals.length);
let randomAnimal = animals[number].toLowerCase(); // Convert the random animal to lowercase
console.log(randomAnimal);

// Variable to store the dashed version of the animal
let dashedAnimal = Array(randomAnimal.length).fill("-");

// Button to start the game and display dashed letters
let start = document.querySelector(".start_button");
start.addEventListener('click', () => {
    document.querySelector(".dashed-letters").textContent = dashedAnimal.join('');
});

// Getting user's input (letter buttons)
var numberOfButtons = document.querySelectorAll(".letter").length;

for (var i = 0; i < numberOfButtons; i++) {
    document.querySelectorAll(".letter")[i].addEventListener("click", function () {
        var guessedLetter = this.innerHTML.toLowerCase();
        console.log(guessedLetter);

        let correctGuess = false;

        // Check if the guessed letter is in the random animal
        for (let j = 0; j < randomAnimal.length; j++) {
            if (guessedLetter === randomAnimal[j]) {
                dashedAnimal[j] = randomAnimal[j]; // Update the dashed word with the correct letter
                correctGuess = true;
            }
        }

        // Update the dashed word on the screen
        document.querySelector(".dashed-letters").textContent = dashedAnimal.join('');

        // Check if the whole word has been guessed
        if (dashedAnimal.join('') === randomAnimal) {
            document.querySelector(".win-or-lose").style.display = "block";
            document.querySelector(".letters").style.display = "none";
            document.querySelector(".dashed-letters").style.display = "none";
            document.querySelector(".win-or-lose").textContent = "Congratulations";
        } else if (!correctGuess) {
            lives -= 1;
            if(lives === 0){
                document.querySelector(".win-or-lose").style.display = "block";
                document.querySelector(".letters").style.display = "none";
                document.querySelector(".dashed-letters").style.display = "none";
                document.querySelector(".win-or-lose").textContent = "Sorry You Lost, Try again "; 
            }
        }
        console.log(lives)
        document.querySelector("img").src = "./Hangman images/hangman"+lives+".png"
        document.querySelector(".restart_button").addEventListener('click',() => {
            location.reload();
        });
    });
}
